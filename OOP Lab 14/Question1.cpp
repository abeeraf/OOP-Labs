#include<cmath>

template<class t1> 
t1 Power(t1 base, t1 exponent)
{
	return pow(base,exponent);
}
template<class t2>
t2 SquareRoot(t2 number)
{
	return pow(number,0.5);
}
template<class t3>
t3 cosine(t3 x)
{
	return cos(x);
}
template<class t4>
t4 sine(t4 x) 
{
	return sin(x);
}

template<class t5> 
t5 tangent(t5 x) 
{
	return tan(x);
}

